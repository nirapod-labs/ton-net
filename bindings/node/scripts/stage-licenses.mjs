// Copies the license and the notice into every package that gets published from
// this directory: the main one and each per-platform binary package.
//
// This is not tidiness. Apache-2.0 section 4 requires a redistribution to carry a
// copy of the license, and the notice with it where one exists, and each of these
// packages is a separate redistribution to a separate consumer. An npm consumer
// never sees the repository root, so a license that stays there reaches nobody.
//
// npm runs this from `prepack`, before the tarball is built. The copies are
// generated and ignored by git.

import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const binding = join(here, "..");
const root = join(binding, "..", "..");

const files = ["LICENSE", "NOTICE"];
for (const name of files) {
  if (!existsSync(join(root, name))) {
    throw new Error(`${name} is missing from the repository root; nothing to stage`);
  }
}

const targets = [binding];
const platforms = join(binding, "npm");
if (existsSync(platforms)) {
  for (const entry of readdirSync(platforms)) {
    const dir = join(platforms, entry);
    if (statSync(dir).isDirectory()) {
      targets.push(dir);
    }
  }
}

for (const target of targets) {
  mkdirSync(target, { recursive: true });
  for (const name of files) {
    copyFileSync(join(root, name), join(target, name));
  }

  // npm ships a LICENSE whether or not an allowlist mentions it, but NOTICE gets
  // no such treatment, so a package with a `files` list has to name it. The
  // per-platform manifests are generated by `napi create-npm-dirs`, so this is
  // applied here rather than edited into them, and survives the next regeneration.
  const manifest = join(target, "package.json");
  const parsed = JSON.parse(readFileSync(manifest, "utf8"));
  if (Array.isArray(parsed.files) && !parsed.files.includes("NOTICE")) {
    parsed.files.push("NOTICE");
    writeFileSync(manifest, `${JSON.stringify(parsed, null, 2)}\n`);
  }
}

// Progress goes to stderr. npm runs this from `prepack`, so anything written to
// stdout lands in front of the JSON that `npm pack --json` is expected to emit and
// makes it unparseable for whatever reads it.
console.error(`staged ${files.join(" and ")} into ${targets.length} package(s)`);
